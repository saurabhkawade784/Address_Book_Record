    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    try
        {Class.forName("java.sql.DriverManager");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/"+addressbook.database,addressbook.user,addressbook.password);
            Statement st=con.createStatement();
            String query;
            int i=0,j=jTable1.getRowCount();
            while(i<j)
            {
                query="insert into "+jLabel1.getText()+" values('"+jTable1.getValueAt(i,0);
                query+="','"+jTable1.getValueAt(i,1);
                query+="','"+jTable1.getValueAt(i,2)+"',";
                query+="'"+jTable1.getValueAt(i,3)+"',";
                query+="'"+jTable1.getValueAt(i,4)+"',";
                query+=""+jTable1.getValueAt(i,5)+","+jTable1.getValueAt(i,6)+")";
                st.executeUpdate(query);
                i++;
            }   
            jButton2.setEnabled(false);
            jButton4.setEnabled(false);
        }   
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    DefaultTableModel dtm= (DefaultTableModel) jTable1.getModel();
    int numRows=jTable1.getRowCount();
    
    dtm.setRowCount(++numRows);
    
    if(numRows!=0)
        numRows--;
    
    dtm.setValueAt(jTextField1.getText().trim().toUpperCase(),numRows,0);
    dtm.setValueAt(jTextField2.getText().trim().toUpperCase(),numRows,1);
    dtm.setValueAt(jTextField3.getText().trim(),numRows,2);
    dtm.setValueAt(jTextField4.getText().trim().toUpperCase(),numRows,3);
    dtm.setValueAt(jTextField5.getText().trim().toUpperCase(),numRows,4);
    String zip=jTextField6.getText().trim();
    
    if(zip.length()==6)
        dtm.setValueAt(zip,numRows,5);
    else
    {
        JOptionPane.showMessageDialog(this,"Enter Correct Zip-Code It");
    }
    dtm.setValueAt(jTextField7.getText().trim(),numRows,6);
        jButton4.setEnabled(true);
        jButton2.setEnabled(true);
    }//GEN-LAST:event_jButton1ActionPerformed

